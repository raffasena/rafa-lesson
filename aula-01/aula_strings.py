# Conversão de Dados String para Inteiro

num = 1
num2: str = "1"

print(f"# Conversão de Dados String para Inteiro : {num + int(num2)}")

# Conversão de Dados Int para String

print(f"# Conversão de Dados Int para String : {str(num) + num2}")

# Atribuição de Valor

print(num)

num = 10

print(num)

num2 = "100"

print(num2)


# Quantidade de caracteres de uma string

print(f"Length do num2 : {num2.__len__()}")

